public class Variables {

    public static void main(String[] args) {

        // variable = a reusable container for a value
        //            a variable behaves as if it was the value it contains

        // primitives types = simple value stored directly in memory (stack)
        // reference types = memory address (stack) that points to an object in memory (heap)

        // primitve = int, long, float, double, char, boolean
        // reference = String, Array, Class, Interface, Enum

        // 2 steps to create a variable
        // 1. declaration
        // 2. initialization / assignment

        int age = 20;
        int year = 2025;
        int quantity = 2;

        double price = 19;
        double pi = 3.14;
        double temp = 36.6;

        char grade = 'A';
        char symbol = '!';
        char currency = '$';

        boolean isActive = true;
        boolean isAvailable = false;

        String name = "John Doe";
        String city = "New York";
        String message = "hi there!";

        System.out.println("Your age is " + age);

    }

}